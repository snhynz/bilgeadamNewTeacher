package newera;

public class MathOperator {
    public static void main(String[] args) {
        int int1 = 10;
        int int2 = 3;
        double result ;

        System.out.println(result = int1 + int2);
        System.out.println(result = int1 * int2);
        System.out.println(result = int1 - int2);
        System.out.println(result = (double)int1 / int2);
        System.out.println(result = int1 % int2);


    }
}
